// 获取所有新闻列表
export const getAllNews = () => {
  return Object.values(newsData);
};

// 根据ID获取新闻详情
export const getNewsById = (id) => {
  return newsData[id] || null;
};

// 根据分类获取新闻
export const getNewsByCategory = (category) => {
  if (category === '全部') {
    return getAllNews();
  }
  return getAllNews().filter(news => news.category === category);
};

// 搜索新闻
export const searchNews = (searchTerm) => {
  const term = searchTerm.toLowerCase();
  return getAllNews().filter(news => 
    news.title.toLowerCase().includes(term) ||
    news.description.toLowerCase().includes(term) ||
    news.tags.some(tag => tag.toLowerCase().includes(term))
  );
};

// 获取新闻分类列表
export const getNewsCategories = () => {
  const categories = ['全部', '平台动态', '产品新闻', '产品更新', '活动信息', '合作动态', '荣誉奖项'];
  return categories;
};
